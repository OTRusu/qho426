x = input('Towards which direction should I paint (up, down, left or right)?\n ')
if x == ('up'):
  print('I am painting in the upward direction!\n ')
elif x == ('down'):
  print('I am painting in the downward direction!\n ')
elif x == ('left'):
  print('I am painting in the left direction!\n ')
elif x == ('right'):
  print('I am painting in the right direction!\n ')
print('Activity completed!')